a=["one","two","three"]
print(a)
a.append("four")
print(a)
a.insert(1,"one and half")  #inserting at a particular index
print(a)
a.pop(1)    #removing the element at the given index
print(a)
a.remove("one")  #removing the given element
print(a)
print(a.count("one"))  #counting the number of occurence of the given element
a.sort()    #sorting the list
print(a)
a.reverse() #reversing the list
print(a)
